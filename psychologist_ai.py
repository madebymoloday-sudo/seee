        current_field = state['current_field']
        
        if current_field == 'composition':
            # –°–æ–±–∏—Ä–∞–µ–º —Å–æ—Å—Ç–∞–≤ –∏–¥–µ–∏
            parts = self.extract_concept_parts(message)
