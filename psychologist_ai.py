    def generate_document(self, concept_hierarchy: Dict, username: str) -> str:
        """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –¥–æ–∫—É–º–µ–Ω—Ç –≤ –≤–∏–¥–µ –∏–µ—Ä–∞—Ä—Ö–∏–∏ —Ç–∞–±–ª–∏—Ü –∏–∑ —Å–∏—Å—Ç–µ–º—ã —É–±–µ–∂–¥–µ–Ω–∏–π"""
        document = f"# –ö–∞—Ä—Ç–∞ –∫–æ–Ω—Ü–µ–ø—Ü–∏–π: {username}\n\n"
        document += f"*–°–æ–∑–¥–∞–Ω–æ: {datetime.now().strftime('%d.%m.%Y %H:%M')}*\n\n"
        document += "---\n\n"
        
        def escape_markdown_table(text: str) -> str:
            """–≠–∫—Ä–∞–Ω–∏—Ä—É–µ—Ç —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã –¥–ª—è —Ç–∞–±–ª–∏—Ü Markdown"""
            if not text:
                return ""
            # –ó–∞–º–µ–Ω—è–µ–º –ø–µ—Ä–µ–Ω–æ—Å—ã —Å—Ç—Ä–æ–∫ –Ω–∞ <br> –¥–ª—è —Ç–∞–±–ª–∏—Ü
            return str(text).replace('|', '\\|').replace('\n', '<br>')
        
        def format_concept_as_table(concept_name: str, level: int = 0) -> str:
            """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –∏–¥–µ—é –≤ –≤–∏–¥–µ —Ç–∞–±–ª–∏—Ü—ã"""
            if concept_name not in concept_hierarchy:
                return ""
            
            data = concept_hierarchy[concept_name]
            
            # –§–æ—Ä–º–∏—Ä—É–µ–º —Ç–∞–±–ª–∏—Ü—É –¥–ª—è –∏–¥–µ–∏
            table = "| –ü–æ–ª–µ | –ó–Ω–∞—á–µ–Ω–∏–µ |\n"
            table += "|------|----------|\n"
            
            # –ù–∞–∏–º–µ–Ω–æ–≤–∞–Ω–∏–µ –∏–¥–µ–∏
            table += f"| **–ù–∞–∏–º–µ–Ω–æ–≤–∞–Ω–∏–µ –∏–¥–µ–∏** | {escape_markdown_table(data['name'])} |\n"
            
            # –°–æ—Å—Ç–∞–≤ –∏–¥–µ–∏
            composition_text = ""
            if data['composition']:
                composition_text = "<br>".join([f"{i+1}. {part}" for i, part in enumerate(data['composition'])])
            else:
                composition_text = "‚Äî"
            table += f"| **–°–æ—Å—Ç–∞–≤ –∏–¥–µ–∏** | {escape_markdown_table(composition_text)} |\n"
            
            # –û—Å–Ω–æ–≤–∞—Ç–µ–ª—å –∏–¥–µ–∏
            founder_text = data['founder'] if data['founder'] else "‚Äî"
            table += f"| **–û—Å–Ω–æ–≤–∞—Ç–µ–ª—å –∏–¥–µ–∏** | {escape_markdown_table(founder_text)} |\n"
            
            # –¶–µ–ª—å –ø–æ—è–≤–ª–µ–Ω–∏—è –∏–¥–µ–∏
            purpose_text = data['purpose'] if data['purpose'] else "‚Äî"
            table += f"| **–¶–µ–ª—å –ø–æ—è–≤–ª–µ–Ω–∏—è –∏–¥–µ–∏** | {escape_markdown_table(purpose_text)} |\n"
            
            # –≠–º–æ—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–µ –ø–æ—Å–ª–µ–¥—Å—Ç–≤–∏—è
            emotional_text = ""
            if data['consequences']['emotional']:
                emotional_text = "<br>".join([f"‚Ä¢ {cons}" for cons in data['consequences']['emotional']])
            else:
                emotional_text = "‚Äî"
            table += f"| **–≠–º–æ—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–µ –ø–æ—Å–ª–µ–¥—Å—Ç–≤–∏—è** | {escape_markdown_table(emotional_text)} |\n"
            
            # –§–∏–∑–∏—á–µ—Å–∫–∏–µ –ø–æ—Å–ª–µ–¥—Å—Ç–≤–∏—è
            physical_text = ""
            if data['consequences']['physical']:
                physical_text = "<br>".join([f"‚Ä¢ {cons}" for cons in data['consequences']['physical']])
            else:
                physical_text = "‚Äî"
            table += f"| **–§–∏–∑–∏—á–µ—Å–∫–∏–µ –ø–æ—Å–ª–µ–¥—Å—Ç–≤–∏—è** | {escape_markdown_table(physical_text)} |\n"
            
            # –í—ã–≤–æ–¥—ã
            conclusions_text = data['conclusions'] if data['conclusions'] else "‚Äî"
            table += f"| **–í—ã–≤–æ–¥—ã** | {escape_markdown_table(conclusions_text)} |\n"
            
            # –ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏
            comments_text = ""
            if data['comments']:
                comments_text = "<br>".join([f"‚Ä¢ {comment}" for comment in data['comments']])
            else:
                comments_text = "‚Äî"
            table += f"| **–ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏** | {escape_markdown_table(comments_text)} |\n"
            
            table += "\n"
            
            # –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –ø–æ–¥-–∫–æ–Ω—Ü–µ–ø—Ü–∏–∏ (–æ–Ω–∏ –∏–¥—É—Ç –ø–æ—Å–ª–µ —Ç–µ–∫—É—â–µ–π —Ç–∞–±–ª–∏—Ü—ã)
            sub_tables = ""
            for sub_concept in data['sub_concepts']:
                sub_tables += format_concept_as_table(sub_concept, level + 1)
            
            return table + sub_tables
        
        # –ù–∞—Ö–æ–¥–∏–º –∫–æ—Ä–Ω–µ–≤—ã–µ –∫–æ–Ω—Ü–µ–ø—Ü–∏–∏ (—Ç–µ, –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ —è–≤–ª—è—é—Ç—Å—è —á–∞—Å—Ç—å—é –¥—Ä—É–≥–∏—Ö)
        root_concepts = []
        all_sub_concepts = set()
        for concept_data in concept_hierarchy.values():
            all_sub_concepts.update(concept_data.get('sub_concepts', []))
        
        for concept_name in concept_hierarchy.keys():
            if concept_name not in all_sub_concepts:
                root_concepts.append(concept_name)
        
        # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –≤—Å–µ –∫–æ—Ä–Ω–µ–≤—ã–µ –∫–æ–Ω—Ü–µ–ø—Ü–∏–∏ –∫–∞–∫ —Ç–∞–±–ª–∏—Ü—ã
        for root in root_concepts:
            document += format_concept_as_table(root, 0)
            document += "\n---\n\n"
        
        return document

