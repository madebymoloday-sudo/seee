        if session_id not in self.state_machine:
            self.state_machine[session_id] = {
                'stage': 'initial',
                'emotions': [],
                'situations': [],
                'current_concept': None,
                'concept_hierarchy': {},
                'current_field': None,
                'conversation_path': []
            }
        return self.state_machine[session_id]
    
