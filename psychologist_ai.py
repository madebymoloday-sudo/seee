    def handle_editing_mode(self, message: str, state: Dict, history: List[Dict], 
                           concept: str, concept_data: Dict, field_name: str) -> Dict:
        """–û–±—Ä–∞–±–æ—Ç–∫–∞ —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –∫–æ–Ω—Ü–µ–ø—Ü–∏–∏"""
